package vipro;

public class ReverseNumbers {
     public static void main(String[] ar)
     {
    	 int i=101;
    	 while(i>0)
    	 {
    		 i--;    // reverse
    		 System.out.print(i+" ");
    	 }
    	 System.out.println(" ");
    	 int j=101;  //  alternate reverse 
    	 while(j>0)
    	 {
    		 j-=2;
    		 System.out.print(j+" ");
    	 }
    	 
     }
}
